# Print introduction to the adlib game
print("Welcome to the Adlib Game!")
print("Fill in the blanks with your favorite things.")

# Ask user about their favorite color
color = input("What is your favorite color? ")

# Ask user about their favorite food
food = input("What is your favorite food? ")

# Ask user about their favorite animal
animal = input("What is your favorite animal? ")

# Ask user about their favorite movie
movie = input("What is your favorite movie? ")

# Ask user about their favorite book
book = input("What is your favorite book? ")

# Ask user about their favorite hobby
hobby = input("What is your favorite hobby? ")

# Ask user about their favorite place
place = input("What is your favorite place? ")

# Display the completed adlib story using user responses
print("\nYour Adlib Story:")
print(f"Once upon a time, there was a person who loved the color {color}.")
print(f"They enjoyed eating {food} and spending time with their favorite animal, the {animal}.")

print(f"One day, they watched the movie '{movie}' and read the book '{book}'.")
print(f"They loved to {hobby} and often visited their favorite place, {place}.")
print("And they lived happily ever after, surrounded by their favorite things.")
